package springboot.reuniones.async;

public class InfoBusca {
    private long idAsistentes;
    private long idReuniones;

    public long getIdAsistentes() {
        return idAsistentes;
    }

    public void setIdAsistente(long idAsisitentes) {
        this.idAsistentes = idAsisitentes;
    }

    public long getIdReuniones() {
        return idReuniones;
    }

    public void setIdReuniones(long idReuniones) {
        this.idReuniones = idReuniones;
    }
}
